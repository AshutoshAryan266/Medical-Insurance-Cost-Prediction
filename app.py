import streamlit as st
import joblib

st.set_page_config(
    page_title="Insurance Cost Estimator",
    page_icon="🏥",
    layout="centered"
)
# Load the trained model and scaler
model = joblib.load("insurance_model.pkl")
scaler = joblib.load("scaler.pkl")
st.sidebar.title("👨‍💻 Developer")

st.sidebar.write("""
### Ashutosh Aryan

🎓 B.Tech CSE Student

📊 Aspiring Data Analyst & Data Scientist | Machine Learning Enthusiast

🧠 Machine Learning Project

⚙️ Algorithm: Linear Regression

💻 Built with:
- Python
- Scikit-Learn
- Streamlit
""")
st.title("🏥 AI-Powered Medical Insurance Cost Estimator")
st.info(
    "Fill in the details below to estimate your medical insurance charges using our trained AI model."
)

age = st.number_input(
    "👤 Age",
    min_value=18,
    max_value=100,
    value=25,
    step=1
)

gender = st.selectbox(
    "🚻 Gender",
    ["Male", "Female"]
)

if gender == "Female":
    is_female = 1
else:
    is_female = 0


bmi = st.number_input(
    "⚖️ BMI",
    min_value=10.0,
    max_value=60.0,
    value=25.0,
    step=0.1
)
if bmi < 15 or bmi > 50:
    st.warning("⚠️ Please verify your BMI value.")

if bmi >= 30:
    bmi_category_Obese = 1
else:
    bmi_category_Obese = 0

children = st.number_input(
    "👶 Number of Children",
    min_value=0,
    max_value=10,
    value=0,
    step=1
)

smoker = st.selectbox(
    "🚬 Smoker",
    ["No", "Yes"]
)

if smoker == "Yes":
    is_smoker = 1
else:
    is_smoker = 0
region = st.selectbox(
    "🌍 Region",
    ["Northeast", "Northwest", "Southeast", "Southwest"]
)

if region == "Southeast":
    region_southeast = 1
else:
    region_southeast = 0

predict = st.button("🔮 Predict Insurance Charges")
if predict:

    scaled_data = scaler.transform([[age, bmi, children]])

    scaled_age = scaled_data[0][0]
    scaled_bmi = scaled_data[0][1]
    scaled_children = scaled_data[0][2]

    input_data = [[
        scaled_age,
        is_female,
        scaled_bmi,
        scaled_children,
        is_smoker,
        region_southeast,
        bmi_category_Obese
    ]]

    prediction = round(model.predict(input_data)[0], 2)

    st.success(f"💰 Estimated Medical Insurance Charges")

    st.metric(
    label="Predicted Cost",
    value=f"₹ {prediction:,.2f}"
    )
    st.caption(
         "🤖 This estimate is generated by a Linear Regression model trained on historical insurance data. Actual insurance charges may vary."
    )
    # Celebration 🎈
    st.balloons()

st.divider()

st.subheader("📊 Model Information")

st.write("""
- **Algorithm:** Linear Regression
- **R² Score:** 0.804
- **Adjusted R² Score:** 0.799
- **Features Used:** Age, Gender, BMI, Children, Smoking Status, Region, BMI Category
""")

